/*
 * How to input data from the user to the program through 
 * the command line
 */


public class CommandLineArguments {
	//command line arguments are inputed through args
	public static void main (String[] args) {

		//args[0] contains the first argument
		System.out.println(args[0]);

		//args[1] contains the second arugment
		System.out.println(args[1]);

		//args[2] contains the third argument
		System.out.println(args[2]);

	}
}